public class Day16 {
    public static void main(String[] args) {
        Scanner input = new Scanner (System.in);
        System.out.print("Masukkan angka 6 :");
        int caca = input.nextInt();
        System.out.print("Masukkan angka 2 :");
        int didi = input.nextInt();
        
        
        int Perkalian = caca * didi;
        System.out.println("Hasil perkalian\t" + Perkalian);
      
        int Pembagian = caca / didi;
        System.out.println("Hasil pembagian\t" + Pembagian);
       
    }
  
  
}
